// Simulación de registros
let registro1 = 8;
let registro2 = 12;

// Intercambio de valores entre los registros
let temp = registro1;
registro1 = registro2;
registro2 = temp;

console.log("Después del intercambio, registro1:", registro1); // Salida: 12
console.log("Después del intercambio, registro2:", registro2); // Salida: 8

